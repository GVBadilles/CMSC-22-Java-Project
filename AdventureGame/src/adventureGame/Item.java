package adventureGame;

/**
 * 
 * class Item
 *
 */
public class Item {
	private String description;
	
	/**
	 * 
	 * @param d		sets the description
	 */
	
	public void setDescription(String d) {
		description = d;
	}
	
	/**
	 * 
	 * @return		gets the description
	 */
	public String getDescription() {
		return description;
	}
}
